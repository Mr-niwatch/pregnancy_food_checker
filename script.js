  // status → 色変換用
  function getClass(status) {
    if (status === "食べてもOK") return "ok";
    if (status === "控えめに")   return "limit";
    if (status === "避ける")     return "avoid";
    if (status === "要注意")     return "caution";
    return "";
  }
  
  // 判定（クラスは getClass）
  function checkFood() {
    var query = document.getElementById("foodInput").value.trim();
    var food  = FOOD_DATA.find(function(item){ return item.name === query; });
  
    var resultDiv = document.getElementById("result");
    if (food) {
      var cls = getClass(food.status);
      resultDiv.innerHTML =
        "<strong>" + food.name + "</strong> → " +
        "<span class='" + cls + "'>" + food.status + "</span><br>" +
        food.reason;
    } else {
      resultDiv.innerHTML = "「" + query + "」についての情報はまだ登録されていません。";
    }
  }
  
  
  // 判定
  document.getElementById("checkBtn").addEventListener("click", checkFood);

